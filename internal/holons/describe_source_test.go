func TestRenderDescribeTemplateFromGoSDKCompiles(t *testing.T) {
	if _, err := execLookPath("go"); err != nil {
		t.Skip("go command not available")
	}

	root := t.TempDir()
	outputPath := filepath.Join(root, "gen", "describe_generated.go")

	rendered, err := renderDescribeTemplate(repoGoDescribeTemplatePath(t), outputPath, sampleDescribeResponse())
	if err != nil {
		t.Fatalf("renderDescribeTemplate failed: %v", err)
	}

	content := string(rendered)
	if !strings.Contains(content, "Code generated by op build. DO NOT EDIT.") {
		t.Fatal("rendered output missing generated header")
	}
	if !strings.Contains(content, "func StaticDescribeResponse() *holonsv1.DescribeResponse") {
		t.Fatal("rendered output missing StaticDescribeResponse")
	}
	if !strings.Contains(content, "test.v1.Echo") {
		t.Fatal("rendered output missing service name")
	}
	if !strings.Contains(content, "FieldLabel_FIELD_LABEL_MAP") {
		t.Fatal("rendered output missing map field label")
	}

	if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		t.Fatalf("mkdir gen: %v", err)
	}
	if err := os.WriteFile(outputPath, rendered, 0o644); err != nil {
		t.Fatalf("write generated source: %v", err)
	}
	if err := os.WriteFile(filepath.Join(root, "go.mod"), []byte(compilationModuleFile(t)), 0o644); err != nil {
		t.Fatalf("write go.mod: %v", err)
	}

	output, err := runCommand(root, []string{"go", "mod", "tidy"})
	if err != nil {
		t.Fatalf("go mod tidy failed: %v\n%s", err, output)
	}

	output, err = runCommand(root, []string{"go", "test", "./gen"})
	if err != nil {
		t.Fatalf("go test ./gen failed: %v\n%s", err, output)
	}
}

func writeGoDescribeTemplate(t *testing.T, root, contents string) {
	t.Helper()

	path := filepath.Join(root, "sdk", "go-holons", "templates", "describe.go.tmpl")
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatalf("mkdir template dir: %v", err)
	}
	if err := os.WriteFile(path, []byte(contents), 0o644); err != nil {
		t.Fatalf("write template: %v", err)
	}
}

func copyRepoGoDescribeTemplate(t *testing.T, root string) {
	t.Helper()

	data, err := os.ReadFile(repoGoDescribeTemplatePath(t))
	if err != nil {
		t.Fatalf("read repo template: %v", err)
	}
	writeGoDescribeTemplate(t, root, string(data))
}

func repoGoDescribeTemplatePath(t *testing.T) string {
	t.Helper()
	return filepath.Join(holonsRepoRoot(t), "sdk", "go-holons", "templates", "describe.go.tmpl")
}

