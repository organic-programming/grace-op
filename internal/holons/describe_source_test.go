func TestGenerateDescribeSourceSkipsWhenTemplateMissing(t *testing.T) {
	root := t.TempDir()
	dir := writeProtoCargoHolonFixture(t, root, "no-template-cargo")

	manifest, err := LoadManifest(dir)
	if err != nil {
		t.Fatalf("LoadManifest failed: %v", err)
	}

	restore, err := generateDescribeSource(manifest, progress.Silence())
	if err != nil {
		t.Fatalf("generateDescribeSource returned error: %v", err)
	}
	t.Cleanup(restore)

	matches, err := filepath.Glob(filepath.Join(dir, "gen", "describe_generated.*"))
	if err != nil {
		t.Fatalf("glob generated files: %v", err)
	}
	if len(matches) != 0 {
		t.Fatalf("expected no generated describe sources, found %v", matches)
	}
}

